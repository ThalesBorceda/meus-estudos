# Escreva um programa que exiba uma mensagem se uma variável for menor do que 10 e outra mensagem se a variável for maior ou igual a 10 

variavel = int(input('Digite um número: ')) 

if (variavel < 10):
    print('O número informado é menor do que 10')
else:
    print('O número informado é igual ou maior do que 10')