# Escreva um programa que permita que o usuário pergunte sua altura, cor favorita ou autor favorito e retorne o resultado a partir do dicionário criado no desafio anterior 

altura = float(input('Qual a sua altura? ')) 
cor_favorita = input('Qual a sua cor favorita? ')
autor_favorito = input('Qual o seu autor favorito? ')

dicionario = {"altura" : altura, "cor_favorita" : cor_favorita, "autor favorito": autor_favorito}

print(dicionario)
